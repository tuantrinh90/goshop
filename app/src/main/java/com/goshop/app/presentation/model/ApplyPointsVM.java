package com.goshop.app.presentation.model;

public class ApplyPointsVM {

    private String pointsTotal;

    private String pointsApplied;

    private String pointsBalance;

    private String priceOriginal;

    private String priceNew;

    public String getPointsTotal() {
        return pointsTotal;
    }

    public void setPointsTotal(String pointsTotal) {
        this.pointsTotal = pointsTotal;
    }

    public String getPointsApplied() {
        return pointsApplied;
    }

    public void setPointsApplied(String pointsApplied) {
        this.pointsApplied = pointsApplied;
    }

    public String getPointsBalance() {
        return pointsBalance;
    }

    public void setPointsBalance(String pointsBalance) {
        this.pointsBalance = pointsBalance;
    }

    public String getPriceOriginal() {
        return priceOriginal;
    }

    public void setPriceOriginal(String priceOriginal) {
        this.priceOriginal = priceOriginal;
    }

    public String getPriceNew() {
        return priceNew;
    }

    public void setPriceNew(String priceNew) {
        this.priceNew = priceNew;
    }
}
